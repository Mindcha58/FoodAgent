import os

from crewai import Agent, Crew, Process, Task
from dotenv import load_dotenv
from langchain_google_genai import ChatGoogleGenerativeAI  # noqa: F401

# 1. โหลดการตั้งค่า (คงเดิม)
load_dotenv("ChatFood.env")
gemini_key = os.getenv("GEMINI_API_KEY")

# สำคัญมาก: สำหรับ litellm ต้องใช้ชื่อตัวแปรนี้เท่านั้น
os.environ["GEMINI_API_KEY"] = gemini_key

# 2. ตั้งค่าสมอง (ใช้ Prefix ว่า gemini/)
llm = "google/gemini-2.5-flash"

# 3. สร้าง Agents ตามโครงสร้างใน PDF
def build_food_crew(user_context):
    is_three_meals = (
        "3 มื้อภายใน 1 วัน" in user_context
        or "มื้อเช้า" in user_context
        or "มื้อกลางวัน" in user_context
        or "มื้อเย็น" in user_context
    )

    planner = Agent(
        role="นักวางแผนมื้ออาหารมือโปร",
        goal="ออกแบบเมนูอาหาร 1 มื้อที่เหมาะกับผู้ใช้จริง",
        backstory="คุณเป็นผู้เชี่ยวชาญด้านโภชนาการ ราคาอาหารไทย และการปรับเมนูตามประวัติผู้ใช้",
        llm=llm,
        verbose=True,
        allow_delegation=False
    )

    accountant = Agent(
        role="ผู้คุมงบประมาณ",
        goal="ตรวจสอบว่าเมนูไม่เกินงบและสรุปยอดเงินคงเหลือ",
        backstory="คุณดูแลงบประมาณรายบุคคลอย่างละเอียด",
        llm=llm,
        verbose=True,
        allow_delegation=False
    )

    if is_three_meals:
        task1 = Task(
            description=f"""
            ข้อมูลผู้ใช้:
            {user_context}

            จงวางแผนเมนูอาหาร 3 มื้อภายใน 1 วัน ได้แก่:
                - มื้อเช้า
                - มื้อกลางวัน
                - มื้อเย็น

            เงื่อนไข:
                - เมนูทั้ง 3 มื้อต้องไม่ซ้ำกัน
                - ต้องตรงกับความชอบของผู้ใช้
                - ต้องหลีกเลี่ยงอาหารที่ผู้ใช้ไม่ชอบและอาหารที่แพ้
                - ต้องเหมาะกับเป้าหมายสุขภาพ
                - ถ้าผู้ใช้เพิ่งปฏิเสธเมนู ให้หลีกเลี่ยงเมนูประเภทเดียวกัน
                - ถ้าผู้ใช้มีเป้าหมายลดน้ำหนัก ให้คุมแคลอรี่โดยประมาณของแต่ละมื้อ
                - ต้องพิจารณาเพศ อายุ น้ำหนัก และส่วนสูงของผู้ใช้ร่วมด้วย
                - ต้องไม่เกินงบรวมที่ผู้ใช้ให้มา
                - ตอบเป็นภาษาไทยแบบเข้าใจง่าย

            ข้อกำหนดเพิ่มเติม:
                - แต่ละมื้อต้องเป็นเมนูมาตรฐานที่ร้านอาหารทั่วไปมีขายจริง
                - หลีกเลี่ยงชื่อเมนูที่เฉพาะเกินไปหรือดัดแปลงมากเกินไป
                - ถ้าต้องการปรับสุขภาพ ให้ระบุเป็นหมายเหตุเพิ่มเติมแยกจากชื่อเมนู

            รูปแบบผลลัพธ์:
                มื้อเช้า: [ชื่อเมนู] | ราคา: [X] บาท | แคลอรี่: [Y] kcal
                มื้อกลางวัน: [ชื่อเมนู] | ราคา: [X] บาท | แคลอรี่: [Y] kcal
                มื้อเย็น: [ชื่อเมนู] | ราคา: [X] บาท | แคลอรี่: [Y] kcal
                งบรวม: [Z] บาท
                เหตุผล: [สั้นๆ]
                """,
            expected_output="เมนูอาหาร 3 มื้อ พร้อมราคา แคลอรี่ และงบรวม",
            agent=planner,
        )

        task2 = Task(
            description="""
            รับผลจาก Task 1 แล้วตรวจสอบว่า:
                - มีครบ 3 มื้อจริง
                - เมนูไม่ซ้ำกัน
                - งบรวมเหมาะสม
                - ถ้าผู้ใช้ต้องการลดน้ำหนัก ให้แคลอรี่ของแต่ละมื้อและรวมทั้งวันเหมาะสม
                - สรุปคำตอบให้อ่านง่าย

            รูปแบบผลลัพธ์:
                มื้อเช้า: [ชื่อเมนู] | ราคา: [X] บาท | แคลอรี่: [Y] kcal
                มื้อกลางวัน: [ชื่อเมนู] | ราคา: [X] บาท | แคลอรี่: [Y] kcal
                มื้อเย็น: [ชื่อเมนู] | ราคา: [X] บาท | แคลอรี่: [Y] kcal
                งบรวม: [Z] บาท
                เหตุผล: [สั้นๆ]
                """,
            expected_output="สรุปเมนู 3 มื้อครบถ้วน",
            agent=accountant,
        )

    else:
        task1 = Task(
            description=f"""
            ข้อมูลผู้ใช้:
                {user_context}

            จงเลือกเมนูอาหาร 1 มื้อที่:
                - ตรงกับความชอบ
                - ไม่ซ้ำกับเมนูล่าสุด
                - ไม่เกินงบ
                - เหมาะกับเป้าหมายสุขภาพ
                - ถ้าผู้ใช้เพิ่งปฏิเสธเมนูก่อนหน้า ห้ามเสนอเมนูเดิมหรือเมนูในหมวดเดียวกัน
                - ถ้าผู้ใช้มีเป้าหมายลดน้ำหนัก ให้เลือกเมนูที่คุมพลังงานได้ดี อิ่มนาน โปรตีนเพียงพอ และระบุแคลอรี่โดยประมาณของมื้อนั้น
                - พิจารณาเพศ อายุ น้ำหนัก และส่วนสูงของผู้ใช้ร่วมด้วย
                - ต้องหลีกเลี่ยงอาหารที่ผู้ใช้ไม่ชอบและอาหารที่แพ้
                - ใช้อาหารที่ชอบของผู้ใช้เป็นตัวช่วยในการตัดสินใจ

            ข้อกำหนดเพิ่มเติม:
                - ให้เลือกเฉพาะเมนูอาหารมาตรฐานที่ร้านอาหารทั่วไปในไทยมีขายจริง
                - หลีกเลี่ยงการตั้งชื่อเมนูเฉพาะหรือดัดแปลงมากเกินไป
                - ชื่อเมนูต้องเป็นคำที่สามารถค้นหาร้านอาหารบนเว็บได้ง่าย
                - ถ้าต้องการปรับสุขภาพ เช่น ไม่เอาหนัง ลดข้าว ไม่ใส่วุ้นเส้น ให้ระบุเป็น "หมายเหตุเพิ่มเติม" ไม่ใช่ใส่ในชื่อเมนูหลัก
                - ตัวอย่างชื่อเมนูที่ดี: ข้าวมันไก่, กะเพราหมูสับไข่ดาว, สุกี้น้ำไก่, ข้าวหมูแดง, ข้าวคลุกกะปิ
                - ตัวอย่างชื่อเมนูที่ไม่ควรใช้: ข้าวมันไก่อกไก่ไม่เอาหนังพิเศษ, สุกี้น้ำไก่ไม่ใส่วุ้นเส้นสูตรคลีน
                - ตอบเป็นภาษาไทยแบบเข้าใจง่าย

            รูปแบบผลลัพธ์:
                สรุปสุดท้าย: [ชื่อเมนู] | ราคา: [X] บาท | แคลอรี่: [Y] kcal | เหตุผล: [สั้นๆ]
            """,
            expected_output="ชื่อเมนูอาหาร พร้อมราคาประเมิน แคลอรี่โดยประมาณ และเหตุผลด้านโภชนาการ",
            agent=planner,
        )

        task2 = Task(
            description="""
            รับผลจาก Task 1 แล้วตรวจสอบว่า:
                - ราคาเหมาะสม
                - ไม่เกินงบ
                - ถ้าผู้ใช้มีเป้าหมายลดน้ำหนัก ให้ตรวจสอบว่าแคลอรี่ของมื้อนั้นไม่สูงเกินไปสำหรับ 1 มื้อ
                - ชื่อเมนูต้องเป็นชื่อเมนูมาตรฐานที่สามารถค้นหาร้านบนเว็บได้ง่าย
                - ถ้ามีการปรับสุขภาพ ให้เก็บไว้ใน "หมายเหตุเพิ่มเติม" ไม่ใช่รวมในชื่อเมนู
                - สรุปคำตอบสุดท้ายให้ชัดเจน

            รูปแบบผลลัพธ์:
                สรุปสุดท้าย: [ชื่อเมนู] | ราคา: [X] บาท | แคลอรี่: [Y] kcal | เหตุผล: [สั้นๆ]
            """,
            expected_output="สรุปสุดท้ายพร้อมชื่อเมนู ราคา แคลอรี่ และเหตุผล",
            agent=accountant,
        )

    return Crew(
        agents=[planner, accountant],
        tasks=[task1, task2],
        process=Process.sequential,
        # เพิ่มการจำกัดความเร็ว เพื่อไม่ให้กระแทก Server ของรุ่นใหม่จนติด 503
        max_rpm=10
    )
